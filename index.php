			<h2>About Us</h2>
				<div class="content">
						<img src="images/aboutimg.png" alt="girl picking fruit from tree">

						<p>Not Far From The Tree puts Toronto’s fruit to good use by picking and sharing the bounty</p>

						<p>When a homeowner can’t keep up with the abundant harvest produced by their tree, they let us know and we mobilize our volunteers to pick the bounty. The harvest is split three ways: 1/3 is offered to the tree owner, 1/3 is shared among the volunteers, and 1/3 is delivered by bicycle to be donated to food banks, shelters, and community kitchens in the neighbourhood so that we’re putting this existing source of fresh fruit to good use. It’s a win-win-win situation!</p>

						<p>This simple act has profound impact. With an incredible crew of volunteers, we’re making good use of healthy food, addressing climate change with hands-on community action, and building community by sharing the urban abundance.</p>

						<p>As a project of Tides Canada Initiatives Society, Not Far From The Tree is committed to the principles of dignity, independence, integration and equal opportunity. We believe that helping to create a country, and an organization, where every person can fully participate benefits our people, our partners and our communities.</p>

						<p>Tides Canada’s Accessibility Policy and Plan outline the various considerations our staff, volunteers and contractors need to take into account when communicating with an individual with a disability. They also outline our guidelines regarding training, what to do in the case of a temporary disruption and how we accept feedback regarding the way Tides Canada has interacted with a person with a disability.</p>
